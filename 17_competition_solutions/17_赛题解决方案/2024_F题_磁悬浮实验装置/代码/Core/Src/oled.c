/**
 * @brief  向OLED发送命令
 * @param  cmd: 命令字节
 */
static void OLED_WriteCmd(uint8_t cmd)
{
    uint8_t data[2] = {0x00, cmd};
    HAL_I2C_Master_Transmit(&hi2c1, OLED_ADDRESS, data, 2, 100);
}

/**
 * @brief  向OLED发送数据
 * @param  data: 数据字节
 */
static void OLED_WriteData(uint8_t data)
{
    uint8_t buf[2] = {0x40, data};
    HAL_I2C_Master_Transmit(&hi2c1, OLED_ADDRESS, buf, 2, 100);
}

/**
 * @brief  OLED初始化
 * @retval 无
 */
void OLED_Init(void)
{
    HAL_Delay(100);  // 等待OLED上电稳定
    
    OLED_WriteCmd(0xAE);  // 关闭显示
    OLED_WriteCmd(0x20);  // 设置内存寻址模式
    OLED_WriteCmd(0x10);  // 页寻址模式
    OLED_WriteCmd(0xB0);  // 设置页地址
    OLED_WriteCmd(0xC8);  // COM扫描方向
    OLED_WriteCmd(0x00);  // 列地址低位
    OLED_WriteCmd(0x10);  // 列地址高位
    OLED_WriteCmd(0x40);  // 起始行地址
    OLED_WriteCmd(0x81);  // 设置对比度
    OLED_WriteCmd(0xCF);  // 对比度值
    OLED_WriteCmd(0xA1);  // 段重映射
    OLED_WriteCmd(0xA6);  // 正常显示
    OLED_WriteCmd(0xA8);  // 多路复用率
    OLED_WriteCmd(0x3F);  // 1/64
    OLED_WriteCmd(0xA4);  // 全局显示开启
    OLED_WriteCmd(0xD3);  // 显示偏移
    OLED_WriteCmd(0x00);  // 无偏移
    OLED_WriteCmd(0xD5);  // 时钟分频
    OLED_WriteCmd(0xF0);  // 最高频率
    OLED_WriteCmd(0xD9);  // 预充电周期
    OLED_WriteCmd(0x22);
    OLED_WriteCmd(0xDA);  // COM引脚配置
    OLED_WriteCmd(0x12);
    OLED_WriteCmd(0xDB);  // VCOMH电压
    OLED_WriteCmd(0x20);
    OLED_WriteCmd(0x8D);  // 电荷泵
    OLED_WriteCmd(0x14);  // 启用
    OLED_WriteCmd(0xAF);  // 开启显示
    
    OLED_Clear();
}

/**
 * @brief  清屏
 * @retval 无
 */
void OLED_Clear(void)
{
    for (uint8_t page = 0; page < 8; page++)
    {
        OLED_WriteCmd(0xB0 + page);
        OLED_WriteCmd(0x00);
        OLED_WriteCmd(0x10);
        for (uint8_t col = 0; col < 128; col++)
        {
            OLED_WriteData(0x00);
        }
    }
}

/**
 * @brief  设置光标位置
 * @param  x: 列地址(0~127)
 * @param  y: 页地址(0~7)
 */
void OLED_SetPos(uint8_t x, uint8_t y)
{
    OLED_WriteCmd(0xB0 + y);
    OLED_WriteCmd(0x0F & x);
    OLED_WriteCmd(0x10 | (x >> 4));
}

/**
 * @brief  显示单个字符
 * @param  x: 列地址(0~20)
 * @param  y: 页地址(0~7)
 * @param  ch: 字符
 */
void OLED_ShowChar(uint8_t x, uint8_t y, char ch)
{
    if (ch < 32 || ch > 126) ch = ' ';
    
    OLED_SetPos(x * 6, y);
    for (uint8_t i = 0; i < 6; i++)
    {
        OLED_WriteData(font6x8[ch - 32][i]);
    }
}

/**
 * @brief  显示字符串
 * @param  x: 起始列地址(0~20)
 * @param  y: 页地址(0~7)
 * @param  str: 字符串指针
 */
void OLED_ShowString(uint8_t x, uint8_t y, const char *str)
{
    while (*str)
    {
        OLED_ShowChar(x, y, *str);
        x++;
        str++;
    }
}

/**
 * @brief  显示数字
 * @param  x: 起始列地址
 * @param  y: 页地址
 * @param  num: 数字
 * @param  len: 数字长度
 */
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len)
{
    char buf[12];
    sprintf(buf, "%*lu", len, num);
    OLED_ShowString(x, y, buf);
}

/**
 * @brief  显示浮点数
 * @param  x: 起始列地址
 * @param  y: 页地址
 * @param  num: 浮点数
 * @param  len: 总长度
 */
void OLED_ShowFloat(uint8_t x, uint8_t y, float num, uint8_t len)
{
    char buf[20];
    sprintf(buf, "%*.1f", len, num);
    OLED_ShowString(x, y, buf);
}

/**
 * @brief  开启显示
 */
void OLED_Display_On(void)
{
    OLED_WriteCmd(0xAF);
}

/**
 * @brief  关闭显示
 */
void OLED_Display_Off(void)
{
    OLED_WriteCmd(0xAE);
}

/**
 * @brief  显示BMP图片
 * @param  x0,y0: 起始坐标
 * @param  x1,y1: 结束坐标
 * @param  bmp: 图片数据
 */
void OLED_DrawBMP(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1, const uint8_t *bmp)
{
    uint16_t j = 0;
    for (uint8_t y = y0; y < y1; y++)
    {
        OLED_SetPos(x0, y);
        for (uint8_t x = x0; x < x1; x++)
        {
            OLED_WriteData(bmp[j++]);
        }
    }
}
